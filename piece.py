class Piece():

    def __init__(self):

        self.x = int(BOARDWIDTH / 2) - int(TEMPLATEWIDTH / 2)
        self.y = -2
        self.shape = random.choice(list(PIECES.keys()))
        self.state = random.randint(0, len(PIECES[self.shape])-1)
        self.color = random.randint(0, len(COLORS)-1)
        self.structure = PIECES[self.shape][self.state]




    def isValidPosition(self, board, dir):
        return True


    def move_right(self):
        self.x += 1

    def move_left(self):
        self.x -= 1

    def move_down(self):
        self.y += 1



